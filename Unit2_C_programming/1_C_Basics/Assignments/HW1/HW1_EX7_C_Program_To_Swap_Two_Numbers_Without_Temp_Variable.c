#include <stdio.h>

void main()
{
    float a = 0.0;
    float b = 0.0;
    printf("Enter value of a: ");
    scanf("%f", &a);
    printf("Enter value of b: ");
    scanf("%f", &b);
    printf("\n");
    printf("After swapping, value of a = %0.2f \n", b);
    printf("After swapping, value of b = %0.1f", a);
}